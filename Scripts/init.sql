    InactivadoPor VARCHAR(150) NULL,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    RequiereCambioPassword BOOLEAN NOT NULL DEFAULT TRUE,
    EsCliente BOOLEAN NOT NULL DEFAULT FALSE
