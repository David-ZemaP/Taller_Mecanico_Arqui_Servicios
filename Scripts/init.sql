CREATE TABLE IF NOT EXISTS ColorVehiculo (
    ColorVehiculoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS Marca (
    MarcaId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS Modelo (
    ModeloId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MarcaId INT NOT NULL,
    Nombre VARCHAR(50) NOT NULL,
    CONSTRAINT FK_Modelo_Marca FOREIGN KEY (MarcaId) REFERENCES Marca(MarcaId),
    CONSTRAINT UQ_Modelo_MarcaNombre UNIQUE (MarcaId, Nombre)
);

CREATE TABLE IF NOT EXISTS Cliente (
    ClienteId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL,
    PrimerApellido VARCHAR(100) NOT NULL,
    SegundoApellido VARCHAR(100),
    Ci INT NOT NULL,
    CiComplemento VARCHAR(2),
    Telefono INT NOT NULL,
    Email VARCHAR(100) NOT NULL UNIQUE,
    UsuarioLoginId INT NULL UNIQUE,
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    FechaActualizacion TIMESTAMP NULL,
    IsDeleted BOOLEAN NOT NULL DEFAULT FALSE,
    FechaRegistro TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    TipoCliente VARCHAR(30) NOT NULL DEFAULT 'Regular'
);

CREATE TABLE IF NOT EXISTS Empleado (
    EmpleadoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL,
    PrimerApellido VARCHAR(100) NOT NULL,
    SegundoApellido VARCHAR(100),
    Ci INT NOT NULL,
    CiComplemento VARCHAR(2),
    Telefono INT NOT NULL,
    Email VARCHAR(100),
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    FechaActualizacion TIMESTAMP NULL,
    IsDeleted BOOLEAN NOT NULL DEFAULT FALSE,
    FechaContratacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    EstadoLaboral VARCHAR(30) NOT NULL DEFAULT 'Activo',
    tipoempleado VARCHAR(50) NOT NULL,
    Especialidad VARCHAR(100) NULL,
    SalarioPorHora DECIMAL(10,2) NULL,
    SalarioMensual DECIMAL(10,2) NULL,
    NivelAcceso VARCHAR(50) NULL
);

CREATE TABLE IF NOT EXISTS Vehiculo (
    VehiculoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ClienteId INT NOT NULL,
    Placa VARCHAR(20) NOT NULL UNIQUE,
    MarcaId INT NOT NULL,
    ModeloId INT NOT NULL,
    ColorVehiculoId INT NOT NULL,
    Anio INT NOT NULL,
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    IsDeleted BOOLEAN NOT NULL DEFAULT FALSE,
    FechaActualizacion TIMESTAMP NULL,
    CONSTRAINT FK_Vehiculo_Cliente FOREIGN KEY (ClienteId) REFERENCES Cliente(ClienteId),
    CONSTRAINT FK_Vehiculo_Marca FOREIGN KEY (MarcaId) REFERENCES Marca(MarcaId),
    CONSTRAINT FK_Vehiculo_Modelo FOREIGN KEY (ModeloId) REFERENCES Modelo(ModeloId),
    CONSTRAINT FK_Vehiculo_Color FOREIGN KEY (ColorVehiculoId) REFERENCES ColorVehiculo(ColorVehiculoId)
);

CREATE TABLE IF NOT EXISTS OrdenTrabajo (
    OrdenTrabajoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VehiculoId INT NOT NULL,
    FechaIngreso TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FechaEntrega TIMESTAMP NULL,
    EstadoTrabajo VARCHAR(30) NOT NULL,
    EstadoPago VARCHAR(30) NOT NULL,
    EstadoVehiculo VARCHAR(30) NOT NULL,
    Total DECIMAL(10,2) NOT NULL,
    Estado INT NOT NULL DEFAULT 1,
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    IsDeleted BOOLEAN NOT NULL DEFAULT FALSE,
    FechaActualizacion TIMESTAMP NULL,
    CONSTRAINT FK_Orden_Vehiculo FOREIGN KEY (VehiculoId) REFERENCES Vehiculo(VehiculoId)
);

ALTER TABLE IF EXISTS OrdenTrabajo
    ADD COLUMN IF NOT EXISTS Estado INT NOT NULL DEFAULT 1;

CREATE TABLE IF NOT EXISTS Producto (
    ProductoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL UNIQUE,
    Precio DECIMAL(10,2) NOT NULL,
    Stock INT NOT NULL DEFAULT 0,
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    IsDeleted BOOLEAN NOT NULL DEFAULT FALSE,
    FechaActualizacion TIMESTAMP NULL
);

CREATE TABLE IF NOT EXISTS Servicio (
    ServicioId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(120) NOT NULL UNIQUE,
    Precio DECIMAL(10,2) NOT NULL CHECK (Precio >= 0),
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    IsDeleted BOOLEAN NOT NULL DEFAULT FALSE,
    FechaActualizacion TIMESTAMP NULL
);

CREATE TABLE IF NOT EXISTS OrdenTrabajoProducto (
    OrdenTrabajoProductoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    OrdenTrabajoId INT NOT NULL,
    ProductoId INT NOT NULL,
    Cantidad INT NOT NULL CHECK (Cantidad > 0),
    PrecioUnitario DECIMAL(10,2) NOT NULL CHECK (PrecioUnitario >= 0),
    Subtotal DECIMAL(10,2) NOT NULL CHECK (Subtotal >= 0),
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    CONSTRAINT FK_OrdenTrabajoProducto_OrdenTrabajo FOREIGN KEY (OrdenTrabajoId) REFERENCES OrdenTrabajo(OrdenTrabajoId) ON DELETE CASCADE,
    CONSTRAINT FK_OrdenTrabajoProducto_Producto FOREIGN KEY (ProductoId) REFERENCES Producto(ProductoId)
);

CREATE INDEX IF NOT EXISTS IX_OrdenTrabajoProducto_OrdenTrabajoId ON OrdenTrabajoProducto (OrdenTrabajoId);
CREATE INDEX IF NOT EXISTS IX_OrdenTrabajoProducto_ProductoId ON OrdenTrabajoProducto (ProductoId);

CREATE TABLE IF NOT EXISTS OrdenTrabajoServicio (
    OrdenTrabajoServicioId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    OrdenTrabajoId INT NOT NULL,
    ServicioId INT NOT NULL,
    Cantidad INT NOT NULL CHECK (Cantidad > 0),
    PrecioUnitario DECIMAL(10,2) NOT NULL CHECK (PrecioUnitario >= 0),
    Subtotal DECIMAL(10,2) NOT NULL CHECK (Subtotal >= 0),
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    CONSTRAINT FK_OrdenTrabajoServicio_OrdenTrabajo FOREIGN KEY (OrdenTrabajoId) REFERENCES OrdenTrabajo(OrdenTrabajoId) ON DELETE CASCADE,
    CONSTRAINT FK_OrdenTrabajoServicio_Servicio FOREIGN KEY (ServicioId) REFERENCES Servicio(ServicioId)
);

CREATE INDEX IF NOT EXISTS IX_OrdenTrabajoServicio_OrdenTrabajoId ON OrdenTrabajoServicio (OrdenTrabajoId);
CREATE INDEX IF NOT EXISTS IX_OrdenTrabajoServicio_ServicioId ON OrdenTrabajoServicio (ServicioId);

CREATE TABLE IF NOT EXISTS OrdenTrabajoMecanico (
    OrdenTrabajoId INT NOT NULL,
    MecanicoId INT NOT NULL,
    FechaAsignacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CreadoPor VARCHAR(150) NULL,
    ActualizadoPor VARCHAR(150) NULL,
    EliminadoPor VARCHAR(150) NULL,
    CONSTRAINT PK_OrdenTrabajoMecanico PRIMARY KEY (OrdenTrabajoId, MecanicoId),
    CONSTRAINT FK_OrdenTrabajoMecanico_OrdenTrabajo FOREIGN KEY (OrdenTrabajoId) REFERENCES OrdenTrabajo(OrdenTrabajoId) ON DELETE CASCADE,
    CONSTRAINT FK_OrdenTrabajoMecanico_Mecanico FOREIGN KEY (MecanicoId) REFERENCES Empleado(EmpleadoId) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS IX_OrdenTrabajoMecanico_MecanicoId ON OrdenTrabajoMecanico (MecanicoId);

CREATE TABLE IF NOT EXISTS OrdenTrabajoFoto (
    OrdenTrabajoFotoId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    OrdenTrabajoId     INT NOT NULL,
    Datos              BYTEA NOT NULL,
    ContentType        VARCHAR(100) NOT NULL,
    NombreArchivo      VARCHAR(255) NOT NULL,
    CreadoPor          VARCHAR(150) NULL,
    ActualizadoPor     VARCHAR(150) NULL,
    EliminadoPor       VARCHAR(150) NULL,
    FechaRegistro      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_OrdenTrabajoFoto_OrdenTrabajo FOREIGN KEY (OrdenTrabajoId) REFERENCES OrdenTrabajo(OrdenTrabajoId) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS IX_OrdenTrabajoFoto_OrdenTrabajoId ON OrdenTrabajoFoto (OrdenTrabajoId);

